#include <iostream>

int main() {
    int n;
    std::cin >> n;

    // 奇数（二进制最低位为1）肯定不能拆分成若干个正整数次幂（2的次幂要求都是偶数）
    // 所以如果是奇数，直接输出 -1
    // n & 1 和 n % 2 == 1 是等价的，判断是否为奇数
    if (n & 1) {
        std::cout << -1 << std::endl;
    } else {
        // 从最高可能位开始，向下判断每一位是否为 1 (10^7 大约在 2^23 到 2^24
        // 之间) 从大到小输出每个2的次幂
        for (int i = 24; i >= 1; --i) {
            // 通过位运算 (n >> i) & 1 取出第 i 位的值
            if ((n >> i) & 1) {
                // 如果该位是 1，则输出对应的2的次幂 (1 << i)
                std::cout << (1 << i) << " ";
            }
        }
        std::cout << std::endl;
    }

    return 0;
}