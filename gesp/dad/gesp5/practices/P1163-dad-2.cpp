#include <cstdio>

/**
 * 题目：P1163 银行贷款
 * 描述：当一个人从银行贷款后，在一段时间内他（她）将不得不每月偿还固定的分期付款。
 *       这个问题要求计算出贷款者向银行支付的利率。假设利率按月累计。
 * 输入：三个用空格隔开的正整数。
 *       第一个整数表示贷款的原值 w0，第二个整数表示每月支付的分期付款金额 w，
 *       第三个整数表示分期付款还清贷款所需的总月数 m。
 * 输出：一个实数，表示该贷款的月利率（用百分数表示），四舍五入精确到 0.1%。
 *
 * 算法：二分查找 (Binary Search)
 * 核心逻辑：每月结算时的债务 = 上月剩余债务 * (1 + 月利率) - 当月还款额。
 *           我们需要找到一个合适的月利率，使得在第 m
 * 个月还款后，剩余债务刚好等于（或小于等于）0。
 */

double w0, w;
int m;

/**
 * check 函数：根据猜测的月利率 x，模拟整个还款过程
 * @param x 当前猜测的月利率
 * @return true 表示在给定利率下，还完 m 个月后仍有欠款（即猜测的利率太高）
 *         false 表示还完 m 个月后刚好还清或超额还款（即猜测的利率太低或刚好）
 */
bool check(double x) {
    double w1 = w0;
    for (int i = 1; i <= m; i++) {
        // 每月结算：原欠款加上产生的利息，再减去当月还款额
        w1 = w1 * (1 + x) - w;
    }
    // 如果最后余额 > 0，说明利率定高了，导致最后还欠钱
    return w1 > 0;
}

int main() {
    // 读取贷款原值、每月还款额、总月数
    scanf("%lf %lf %d", &w0, &w, &m);

    // 二分查找范围：0.0 到 3.0 (题目保证答案不超过 300%)
    double l = 0, r = 3;

    // 精度控制：当左右边界的差值小于 0.0001 (0.01%)
    // 时，认为已经达到了题目要求的精确度 (0.1%)
    while (l < r - 0.0001) {
        double mid = l + (r - l) / 2;
        if (check(mid)) {
            // 如果欠款 > 0，说明猜测的利率太高，应该在左半区继续找更小的利率
            r = mid;
        } else {
            // 如果欠款 <=
            // 0，说明猜测的利率太低（或刚好），应该在右半区继续找更大的利率
            l = mid;
        }
    }

    // 题目要求输出百分数，所以要把计算出的小数形式的利率乘以 100
    // 例如：计算出的 l = 0.029，乘以 100 后变为 2.9
    // %.1f 会自动进行四舍五入并保留一位小数
    printf("%.1f\n", l * 100);
    return 0;
}